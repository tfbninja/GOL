    public void drawBlocks() {
        Block bg = new Block();
        bg.setColor(Color.BLACK);
        bg.setPos(0, 0);
        bg.setWidth(this.width);
        bg.setHeight(this.height);
        bg.draw(canvas);
        for (int x = 0; x < this.grid.getWidth(); x++) {
            yPos = 10;
            for (int y = 0; y < this.grid.getLength(); y++) {
                Block temp = new Block();
                if (this.grid.getCell(x, y)) {
                    temp.setColor(Color.web("d84a08")); // orangish
                } else {
                    temp.setColor(Color.web("5d5f63")); // grey
                }
                temp.setX(xPos);
                temp.setY(yPos);
                temp.setWidth(size);
                temp.setHeight(size);
                temp.draw(canvas);
                yPos += margin + size;
            }
            xPos += margin + size;
        }
        yPos = 10;
        xPos = 10;
        this.playPause.draw(canvas);
        this.clear.draw(canvas);
        this.reset.draw(canvas);
        GraphicsContext gc = canvas.getGraphicsContext2D();
        gc.setFill(Color.web("000000"));
        gc.setStroke(Color.RED);
        gc.setLineWidth(2);
        gc.setFont(Font.font("Verdana", 30));
        gc.strokeText("→", playButtonX + 9, buttonY + 23);
        gc.fillText("CLEAR", clearButtonX + 10, buttonY + 26);
        gc.fillText("RESET", resetButtonX + 10, buttonY + 26);
        gc.setFont(Font.font("Arial", 15));
        gc.setFill(Color.WHITE);
        gc.setLineWidth(1);
        gc.fillText("Instructions: Hold any key to play at full speed, left-click a cell to toggle, right click to toggle\n the column of cells, and middle click to spawn a glider.", 10, buttonY - 28);
        //gc.strokeText("Hold any key to play at full speed", this.width / 2 - 95, buttonY - 10);
    }

    public void mouseClicked(MouseEvent e) {
        double mouseX = e.getX();
        double mouseY = e.getY();
        int mX = (int) mouseX;
        int mY = (int) mouseY;
        // top right:
        // margin * x + xPos + (size * (x-1)) : += size
        //solve:
        //margin * (x+1)) + (size * (x-1)) = z, z = margin * x + xPos + margin + size * x - size, z = x(margin + size) + xPos + margin - size, (z + size - margin)/(margin + size) = x

        int xVal = (mX + size - xPos) / (margin + size) - 1;
        int yVal = (mY + size - yPos) / (margin + size) - 1;
        boolean leftClick = e.isPrimaryButtonDown();
        if (leftClick) {
            if (mX >= playButtonX && mX <= playButtonX + buttonW && mY >= buttonY && mY <= buttonY + buttonH) {
                if (this.playPause.getColor().toString().contains(on)) {
                    this.playPause.setColor(Color.web(off));
                    this.play = false;
                } else {
                    this.play = true;
                    this.playPause.setColor(Color.web(on));

                }
                grid.nextGen();
            } else if (mX >= clearButtonX && mX <= clearButtonX + clear.getWidth() && mY >= buttonY && mY <= buttonY + clear.getHeight()) {
                this.grid.clear();
            } else if (mX >= resetButtonX && mX <= resetButtonX + reset.getWidth() && mY >= buttonY && mY <= buttonY + reset.getHeight()) {
                System.out.println("Reset");
                this.grid.clear();
                this.grid.revertToSaved();
            } else {

                try {
                    grid.setCell(xVal, yVal, !grid.getCell(xVal, yVal));
                } catch (ArrayIndexOutOfBoundsException x) {
                    try {
                        Scanner chop = new Scanner(x.getLocalizedMessage());
                        System.out.println("Tried to click " + chop.nextInt());
                    } catch (NullPointerException v) {
                        System.out.println("wack");
                    }
                }
            }
        } else if (e.isSecondaryButtonDown()) {
            // make line crossing the grid
            this.grid.addLine(xVal);
        } else {
            // make glider
            this.grid.addGlider(xVal, yVal, false, 2);
        }
    }

